--[[
Вот ключевые различия между режимами открытия файлов, которые определяют права на чтение, запись и поведение указателя.
## Разница между "r" и "w" (Базовые режимы)

* "r" (Read): Только чтение.
* Файл должен существовать, иначе возникнет ошибка.
   * Указатель встает в начало файла.
   * Содержимое файла защищено от удаления.
* "w" (Write): Только запись.
* Если файл существует, его содержимое полностью удаляется (очищается).
   * Если файла нет, создается новый пустой файл.
   * Указатель встает в начало файла.

## Разница между "r" и "r+" (Расширение для чтения)

* "r": Строго чтение. Запись запрещена.
* "r+" (Read/Write): Чтение и запись одновременно.
* Файл должен существовать. Ошибка, если файла нет.
   * Указатель встает в начало файла.
   * Старые данные перезаписываются поверх новыми символами (без автоматического удаления остатка файла).

## Разница между "w" и "w+" (Расширение для записи)

* "w": Строго запись. Чтение запрещено.
* "w+" (Write/Read): Запись и чтение одновременно.
* Если файл существует, его содержимое сразу удаляется.
   * Если файла нет, создается новый.
   * Позволяет читать то, что вы только что записали в этот файл.

## Сводная таблица для быстрого сравнения

| Режим | Чтение | Запись | Создает файл? | Очищает файл? | Начальная позиция указателя |
|---|---|---|---|---|---|
| "r" | ✅ Да | ❌ Нет | ❌ Нет (Ошибка) | ❌ Нет | Начало файла |
| "r+" | ✅ Да | ✅ Да | ❌ Нет (Ошибка) | ❌ Нет | Начало файла |
| "w" | ❌ Нет | ✅ Да | ✅ Да | ✅ Да | Начало файла (после очистки) |
| "w+" | ✅ Да | ✅ Да | ✅ Да | ✅ Да | Начало файла (после очистки) |

## Важный нюанс при работе с "r+" и "w+"
При переключении между чтением и записью в режимах со знаком + (в таких языках, как C, C++ или Python) необходимо принудительно смещать указатель с помощью функции seek() или очищать буфер с помощью flush(). В противном случае поведение системы может стать непредсказуемым.
